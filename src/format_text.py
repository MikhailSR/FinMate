import re


def fix_text(text: str) -> str:
    # 1. Убираем переносы: слово- \n слово → словослово
    text = re.sub(r'-\n*\s(\s*)', '', text)

    # 2. Склеиваем строки внутри одного абзаца (одиночный перенос строки → пробел)
    text = re.sub(r'(?<!\n)\n(?!\n)', ' ', text)

    # 3. Убираем лишние пробелы внутри строк
    text = re.sub(r' +', ' ', text)

    # 4. Несколько пустых строк подряд → одна пустая строка
    text = re.sub(r'\n{3,}', '\n\n', text)

    return text.strip()
